package com.wk.p3.greenmall.modules.sys.security;

import com.wk.p3.greenmall.modules.user.LoginType;

/**
 * 用户和密码（包含验证码）令牌类
 * @author ThinkGem
 * @version 2013-5-19
 */
public class UsernamePasswordToken extends org.apache.shiro.authc.UsernamePasswordToken {

	private static final long serialVersionUID = 1L;

	private String captcha;
	private boolean mobileLogin;
	private LoginType loginType;

	public UsernamePasswordToken() {
		super();
	}

	public UsernamePasswordToken(String username, char[] password,
			boolean rememberMe, String host, String captcha, boolean mobileLogin) {
		super(username, password, rememberMe, host);
		this.captcha = captcha;
		this.mobileLogin = mobileLogin;
	}

	public UsernamePasswordToken(String username, char[] password,
								 boolean rememberMe, String host, String captcha, LoginType loginType) {
		super(username, password, rememberMe, host);
		this.captcha = captcha;
		this.loginType = loginType;
	}

	public UsernamePasswordToken(String username, String password,boolean rememberMe,LoginType loginType) {
		super(username, password, rememberMe, "");
		this.loginType = loginType;
	}

	public String getCaptcha() {
		return captcha;
	}

	public void setCaptcha(String captcha) {
		this.captcha = captcha;
	}

	public boolean isMobileLogin() {
		return mobileLogin;
	}

	public LoginType getLoginType() {
		return loginType;
	}

	public void setLoginType(LoginType loginType) {
		this.loginType = loginType;
	}
}